#Entrada de datos
numero1 = 3
numero2 = 3

"""SUMA"""
resultado_suma = numero1 + numero2
"""RESTA"""
resultado_resta = numero1 - numero2
"""MULTIPLICACION"""
resultado_multiplicacion = numero1 * numero2
"""DIVISION"""
resultado_division = numero1 / numero2
"""POTENCIACION"""
resultado_potencia = numero1 ** numero2

#SALIDA

print(f"SUMA: {numero1}+{numero2} = {resultado_suma}\nRESTA: {numero1}-{numero2} = {resultado_resta}\nMULTIPLICACION: {numero1}x{numero2} = {resultado_multiplicacion}\nDIVISION: {numero1}/{numero2} = {resultado_division}\nPOTENCIA: {numero1}^{numero2} = {resultado_potencia}")